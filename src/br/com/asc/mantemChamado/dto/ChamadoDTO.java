package br.com.asc.mantemChamado.dto;
/**
 * Classe DTO para dados do chamado, com seus gets and sets.
 * @author Vanderlei Barbosa
 *
 */
public class ChamadoDTO {
	
	private String userId;
	private long numero;
	private String status;
	private String sumarioIncidente;
	private String detalhamento;
	private String registroDeTrabalho;
	private String incidente;
	private String categoria;
	private String classe;
	private String descricaoSolucao;
	private int tempoAtendimento;
	private String dataAbertura;
	private String numeroDePatrimonio;
	private String componente;
	private String problema;
	private String tecnico;
	private String tempoFila;
	private String ordenacao;
	private String nome;
	private String dataInicial;
	private String dataFinal;
	
	
	public String getClasse() {
		return classe;
	}
	public void setClasse(String classe) {
		this.classe = classe;
	}
	public String getIncidente() {
		return incidente;
	}
	public void setIncidente(String incidente) {
		this.incidente = incidente;
	}
	public long getNumero() {
		return numero;
	}
	public void setNumero(long numero) {
		this.numero = numero;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String problema) {
		this.categoria = problema;
	}
	public String getRegistroDeTrabalho() {
		return registroDeTrabalho;
	}
	public void setRegistroDeTrabalho(String registroDeTrabalho) {
		this.registroDeTrabalho = registroDeTrabalho;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getComponente() {
		return componente;
	}
	public void setComponente(String tipoProblema) {
		this.componente = tipoProblema;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getProblema() {
		return problema;
	}
	public void setProblema(String empresa) {
		this.problema = empresa;
	}
	public String getDetalhamento() {
		return detalhamento;
	}
	public void setDetalhamento(String detalhamento) {
		this.detalhamento = detalhamento;
	}
	public String getSumarioIncidente() {
		return sumarioIncidente;
	}
	public void setSumarioIncidente(String sumarioIncidente) {
		this.sumarioIncidente = sumarioIncidente;
	}
	public String getNumeroDePatrimonio() {
		return numeroDePatrimonio;
	}
	public void setNumeroDePatrimonio(String numeroDePatrimonio) {
		this.numeroDePatrimonio = numeroDePatrimonio;
	}
	public String getDataAbertura() {
		return dataAbertura;
	}
	public void setDataAbertura(String dataAbertura) {
		this.dataAbertura = dataAbertura;
	}
	public int getTempoAtendimento() {
		return tempoAtendimento;
	}
	public void setTempoAtendimento(int tempoAtendimento) {
		this.tempoAtendimento = tempoAtendimento;
	}
	public String getDescricaoSolucao() {
		return descricaoSolucao;
	}
	public void setDescricaoSolucao(String descricaoSolucao) {
		this.descricaoSolucao = descricaoSolucao;
	}
	public String getTecnico() {
		return tecnico;
	}
	public void setTecnico(String tecnico) {
		this.tecnico = tecnico;
	}
	public String getTempoFila() {
		return tempoFila;
	}
	public void setTempoFila(String tempoFila) {
		this.tempoFila = tempoFila;
	}
	public String getOrdenacao() {
		return ordenacao;
	}
	public void setOrdenacao(String ordenacao) {
		this.ordenacao = ordenacao;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getDataFinal() {
		return dataFinal;
	}
	public void setDataFinal(String dataFinal) {
		this.dataFinal = dataFinal;
	}
	public String getDataInicial() {
		return dataInicial;
	}
	public void setDataInicial(String dataInicial) {
		this.dataInicial = dataInicial;
	}
}
